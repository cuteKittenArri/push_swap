#include "libft.h"

int	ft_isnum_pm(char *str){
	size_t	i;
	
	i = 1;
	if (!('0' <= str[0] && str[0] <= '9') || str[0] != '-')
		return (0);
	while (str[i])
	{
		if (!('0' <= str[i] && str[i] <= '9'))
			return (0);
		i++;
	}
	return (1);
}